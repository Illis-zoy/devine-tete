function chooseCategory() {
  const selectedCategory = selectedCategoryKey;

  let categoryKey;

  if (selectedCategory === "random") {
    const possibleCategories = Object.keys(categories);
    const randomIndex = Math.floor(Math.random() * possibleCategories.length);
    categoryKey = possibleCategories[randomIndex];
  } else {
    categoryKey = selectedCategory;
  }

  currentWords = categories[categoryKey].words;
  currentCategoryLabel = categories[categoryKey].label;

  if (remainingWords.length === 0) {
    remainingWords = shuffleArray([...currentWords]);
  }

  currentCategoryElement.textContent = currentCategoryLabel;
}

function shuffleArray(array) {
  return array.sort(() => Math.random() - 0.5);
}

